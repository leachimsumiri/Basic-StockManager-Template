CREATE TABLE STOCKS 
(ID INTEGER NOT NULL AUTO_INCREMENT,
NAME VARCHAR(255) NOT NULL,
TICKER VARCHAR(255) NOT NULL,
PRIMARY KEY (id));

CREATE TABLE TIMESERIES 
(ID INTEGER NOT NULL AUTO_INCREMENT,
R_STOCK INTEGER NOT NULL, 
MARKETDATE DATE,
VALUE NUMBER, 
PRIMARY KEY (ID),
FOREIGN KEY (R_STOCK) REFERENCES STOCKS(ID)
);


